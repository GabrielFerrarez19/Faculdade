import numpy as np 
m8 = np.array([1,2,3,4,5])
print(f"matriz m8: {m8}")
print()
#somando a matriz com um escalar
print(f"m8 + 2: {m8 + 2}")
#subtração de matriz com um escalar
print(f"m8 - 2: {m8 - 2}")
#multiplicando por escalar
print(f"m8 * 2: {m8 * 2}")
#divisão de matriz por um escalar
print(f"m8 / 2: {m8 / 2}")
#elevando a matriz a uma potência
print(f"m8 ** 2: {m8 ** 2}")